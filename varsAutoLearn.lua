noXpFromAutoQuests = false -- Variable de control - No cambiar

function learn(player, spell_id)
    --player:SendBroadcastMessage("|CFF00FF00Intentando aprender: " .. spell_id)
    if not player:HasSpell(spell_id) then
        player:LearnSpell(spell_id)
    end
end

function doQuest(player, quest_id)
    --player:SendBroadcastMessage("|CFF00FF00Intentando completar quest: " .. quest_id)
    if not player:GetQuestRewardStatus(quest_id) then
        noXpFromAutoQuests = true --> Detenemos la ganancia de XP
        player:AddQuest(quest_id)
        player:CompleteQuest(quest_id)
        player:RewardQuest(quest_id)
        noXpFromAutoQuests = false --> Habilitamos nuevamente la ganancia de XP
    end
end

function getClassString(mask)
    if mask == 1 then
        return 'WARRIOR'
    elseif mask == 2 then
        return 'PALADIN'
    elseif mask == 8 then
        return 'ROGUE'
    elseif mask == 16 then
        return 'PRIEST'
    elseif mask == 64 then
        return 'SHAMAN'
    elseif mask == 128 then
        return 'MAGE'
    elseif mask == 256 then
        return 'WARLOCK'
    elseif mask == 1024 then
        return 'DRUID'
    end
end

function stopXP(E, player, amount, victim, source)
    -- Si la variable noXpFromAutoQuests es true, el jugador no podrá recibir XP.
    if noXpFromAutoQuests then
        return 0
    end
end

